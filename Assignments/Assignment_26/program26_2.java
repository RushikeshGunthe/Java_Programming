class Pattern 
{
    public void Display(int iNo)
    {
        int iCnt = 0;
       
        for(iCnt = iNo; iCnt >= 1; iCnt--)
        {
            System.out.print( iCnt + "\t # \t");
            
        }
       
    }
}
