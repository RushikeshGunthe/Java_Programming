import java.util.*;


class program33_3
{

    public static void main(String args[]) 
    {
        boolean bRet;
        
        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter a character: ");
        char ch = sobj.next().charAt(0);
        
        Pattern pobj = new Pattern();

        bRet = pobj.ChkDigit(ch);

        if(bRet == true)
        {
            System.out.println("Entered charecter is a Digit");
        }
        else
        {
            System.out.println("Entered charecter is not a Digit");
        }
        
    }
}


